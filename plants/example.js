function fakeBin(x) {
    const num = [];

    for (let i = 0; i < x.length; i++) {
        if (x[i] < 5) {
            num.push(0)
        } else if (x[i] > 5) {
            num.push(1)
        }
    }
    let a = num.join('');
    return a;
}



